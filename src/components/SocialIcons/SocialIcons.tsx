// Icon mapping for the main component
const PLATFORM_ICONS: Record<SocialPlatform, React.FC<SocialIconProps>> = {
    discord: DiscordIcon,
    x: XIcon,
    telegram: TelegramIcon,
    reddit: RedditIcon,
    youtube: YoutubeIcon
};

// Main component that renders multiple social icons
export const SocialIcons: React.FC<SocialIconsProps> = ({ links, size = 24, className }) => {
    return (
        <div className={`social-icons ${className || ''}`}>
            {links.map((link) => {
                const Icon = PLATFORM_ICONS[link.platform];
                return (
                    <Icon
                        key={`${link.platform}-${link.url}`}
                        {...link}
                        size={size}
                    />
                );
            })}
        </div>
    );
};
